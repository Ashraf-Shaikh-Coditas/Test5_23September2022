package Test5_23Sep2022;

public class Teacher {
    private int teacherId;
    private String firstName;
    private String lastName;
    private int flatNo;
    private String city;
    private int pin;
    private float salary;

    public Teacher(int teacherId, String firstName, String lastName, int flatNo, String city, int pin, float salary) {
        this.teacherId = teacherId;
        this.firstName = firstName;
        this.lastName = lastName;
        this.flatNo = flatNo;
        this.city = city;
        this.pin = pin;
        this.salary = salary;
    }


}
